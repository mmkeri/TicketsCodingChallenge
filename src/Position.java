public class Position {

    private int xCoord;
    private int yCoord;

    public Position(int x, int y){
        xCoord = x;
        yCoord = y;
    }

    public int getxCoord(){
        return xCoord;
    }

    public int getyCoord(){
        return yCoord;
    }
}
